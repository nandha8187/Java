package important;

public class Patten_Program1 {

	
	public static void main(String[] args) {
		
		
		
		/*
		 * output : 1 1 1
		 * 
		 *          2 2 2         3 row 3 Column
		 * 
		 *          3 3 3
		 */		            
		
		
		for (int i = 1; i <=3; i++) {      //-----> row
			
			for (int j = 1; j <=3; j++) {   //----->column
				
				System.out.print(i+" ");         //i,j customized data
				
			}
			
			System.out.println();
		}
		
		
		
		
		
		
	}
	
	
	
}
